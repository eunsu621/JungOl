# 정수형 변수 2개에 -1과 100을 대입한 후 아래와 같이 출력하는 프로그램을 작성하라.
x = -1
y = 100
print(x)
print(y)

# 두 개의 변수에 값을 대입하여 아래와 같이 출력되는 프로그램을 작성하라.
x = 55 - 10
y = 2008 - 1999
print("55 - 10= ", x)
print("2008 - 1999= ", y)

